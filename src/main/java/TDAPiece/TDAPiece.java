package TDAPiece;

public class TDAPiece {
    private String color;

    //Constructor
    public TDAPiece(String color) {
        this.color = color;
    }

    public TDAPiece() {
    }

    //Getter's
    public String getColor() {
        return color;
    }

    //Setter's
    public void setColor(String color) {
        this.color = color;
    }
}
